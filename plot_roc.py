def retrn_fp_tp(test_logit,score,test_length):
    correct = 0
    tp_correct = 0
    tp_neg = 0
    correct_predict_death = []
    for i in range(test_length):
        if test_logit[i] == 1:
            tp_correct += 1
        if test_logit[i] == 0:
            tp_neg += 1
        if score[i] < 0 and test_logit[i] == 0:
            correct += 1
        if score[i] > 0 and test_logit[i] == 1:
            correct_predict_death.append(i)
            correct += 1

    acc = np.float(correct) / test_length

    tp_test = 0
    fp_test = 0
    fn_test = 0
    for i in range(test_length):
        if score[i] > 0 and test_logit[i] == 1:
            tp_test += 1
        if score[i] < 0 and test_logit[i] == 1:
            fn_test += 1
        if score[i] > 0 and test_logit[i] == 0:
            fp_test += 1

    precision_test = np.float(tp_test) / (tp_test + fp_test)
    recall_test = np.float(tp_test) / (tp_test + fn_test)
    f1_test = 2 * (precision_test * recall_test) / (precision_test + recall_test)

    threshold = -1.01
    resolution = 0.01
    tp_total = []
    fp_total = []
    precision_total = []
    recall_total = []

    while (threshold < 1.01):
        tp_test = 0
        fp_test = 0
        fn_test = 0
        for i in range(test_length):
            if test_logit[i] == 1 and score[i] > threshold:
                tp_test += 1
            if test_logit[i] == 0 and score[i] > threshold:
                fp_test += 1
            if score[i] < threshold and test_logit[i] == 1:
                fn_test += 1

        tp_rate = tp_test / tp_correct
        fp_rate = fp_test / tp_neg
        if (tp_test + fp_test) == 0:
            precision_test = 1.0
        else:
            precision_test = np.float(tp_test) / (tp_test + fp_test)
        recall_test = np.float(tp_test) / (tp_test + fn_test)
        tp_total.append(tp_rate)
        fp_total.append(fp_rate)
        precision_total.append(precision_test)
        recall_total.append(recall_test)
        threshold += resolution

    return tp_total,fp_total,precision_total,recall_total

